package main

import (
	"github.com/gkawamoto/go-libgba/display"
	"github.com/gkawamoto/go-libgba/display/mode4"
)

var index int = 0
var frame uint8 = 0
var palette = map[uint16]uint8{}

func main() {
	display.EnableVBlankInterrupts()
	display.SetDisplayOptions(display.Mode4, display.BG2)

	// Loading colours from bitmap image to GBA's palette.
	// In real life this should be pre-rendered and stored as an 8 bit array and not as a bitmap
	var count uint8 = 0
	for _, pix := range kirby {
		_, ok := palette[pix]
		if !ok {
			palette[pix] = count
			count++
		}
	}

	for color, index := range palette {
		mode4.SetPalette(uint8(index), color)
	}

	drawImageMode4(palette, kirby, 21, 19, 210, 21*index, 0, true)

	display.RegisterVBlankUpdate(update)
	select {}
}

func update() {
	frame++
	if frame%6 == 0 {
		drawImageMode4(palette, kirby, 21, 19, 210, 21*index, 0, true)
		index = (index + 1) % 10
	}
}

func drawImageMode4(palette map[uint16]uint8, image []uint16, width, height, imageWidth, offsetX, offsetY int, frontPage bool) {
	var x, y int
	for x = 0; x < width; x++ {
		for y = 0; y < height; y++ {
			value := image[(x+imageWidth*y)+(offsetX+offsetY)]
			index := palette[value]
			mode4.SetPixel(uint8(x), uint8(y), index, frontPage)
		}
	}
}

